package Iliatar.ship.modules.fabrics;

class ShipModuleBlueprint {
    private String name;
    private int endurance;
    private int size;
    private int mass;
    private double armor;
    private int caliber;
    private int armorPenetration;
    private int functionSpeed;
    private String aimingModuleName;
    private String ammoLoaderName;
    private String barrellName;
    private int barrellCount;
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getEndurance() {
        return endurance;
    }

    public void setEndurance(int endurance) {
        this.endurance = endurance;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getMass() {
        return mass;
    }

    public void setMass(int mass) {
        this.mass = mass;
    }

    public double getArmor() {
        return armor;
    }

    public void setArmor(double armor) {
        this.armor = armor;
    }

    public int getCaliber() {
        return caliber;
    }

    public void setCaliber(int caliber) {
        this.caliber = caliber;
    }

    public int getArmorPenetration() {
        return armorPenetration;
    }

    public void setArmorPenetration(int armorPenetration) {
        this.armorPenetration = armorPenetration;
    }

    public int getFunctionSpeed() {
        return functionSpeed;
    }

    public void setFunctionSpeed(int functionSpeed) {
        this.functionSpeed = functionSpeed;
    }

    public String getAimingModuleName() {
        return aimingModuleName;
    }

    public void setAimingModuleName(String aimingModuleName) {
        this.aimingModuleName = aimingModuleName;
    }

    public String getAmmoLoaderName() {
        return ammoLoaderName;
    }

    public void setAmmoLoaderName(String ammoLoaderName) {
        this.ammoLoaderName = ammoLoaderName;
    }

    public String getBarrellName() {
        return barrellName;
    }

    public void setBarrellName(String barrellName) {
        this.barrellName = barrellName;
    }

    public int getBarrellCount() {
        return barrellCount;
    }

    public void setBarrellCount(int barrellCount) {
        this.barrellCount = barrellCount;
    }
}
